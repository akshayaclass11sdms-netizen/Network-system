import streamlit as st
import pandas as pd
import joblib
import plotly.express as px

# Load Model
model = joblib.load("traffic_model (2).pkl")

# Congestion Classification
def classify_congestion(vehicle_count):

    if vehicle_count < 50:
        return "Low Traffic"

    elif vehicle_count < 100:
        return "Moderate Traffic"

    elif vehicle_count < 200:
        return "Heavy Traffic"

    else:
        return "Severe Congestion"

# Route Recommendation
def recommend_route(routes):
    return min(routes, key=routes.get)

# Dashboard Title
st.title("Smart Traffic Congestion Prediction System")

st.subheader("Enter Traffic Details")

# User Inputs
junction = st.selectbox(
    "Select Junction",
    [1, 2, 3, 4]
)

hour = st.slider(
    "Hour",
    0,
    23,
    8
)

prev_hour = st.number_input(
    "Previous Hour Vehicles",
    min_value=0,
    value=120
)

prev_2hour = st.number_input(
    "Previous 2 Hour Vehicles",
    min_value=0,
    value=110
)

rolling_mean = st.number_input(
    "Rolling Mean",
    min_value=0.0,
    value=115.0
)

# Predict Button
if st.button("Predict Traffic"):

    input_data = pd.DataFrame({
        'Junction':[junction],
        'Year':[2017],
        'Month':[6],
        'Day':[15],
        'Hour':[hour],
        'DayOfWeek':[3],
        'IsWeekend':[0],
        'PeakHour':[1 if (7 <= hour <= 10 or 17 <= hour <= 20) else 0],
        'Session':[0],
        'Prev_Hour_Vehicles':[prev_hour],
        'Prev_2Hour_Vehicles':[prev_2hour],
        'Rolling_Mean_3':[rolling_mean]
    })

    prediction = model.predict(input_data)[0]

    congestion = classify_congestion(prediction)

    st.success(
        f"Predicted Vehicles: {round(prediction)}"
    )

    st.info(
        f"Congestion Level: {congestion}"
    )

    # Sample Route Predictions
    routes = {
        "Route A": prediction,
        "Route B": prediction * 0.8,
        "Route C": prediction * 0.6
    }

    best_route = recommend_route(routes)

    st.warning(
        f"Recommended Route: {best_route}"
    )

# Traffic Visualization
st.subheader("Traffic Trend Visualization")

df_chart = pd.DataFrame({
    'Hour':[6,7,8,9,10,11,12],
    'Vehicles':[50,80,120,180,150,100,70]
})

fig = px.line(
    df_chart,
    x='Hour',
    y='Vehicles',
    title='Traffic Volume by Hour'
)

st.plotly_chart(fig)
