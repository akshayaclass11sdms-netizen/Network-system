def classify_congestion(vehicle_count):

    if vehicle_count < 50:
        return "Low Traffic"

    elif vehicle_count < 100:
        return "Moderate Traffic"

    elif vehicle_count < 200:
        return "Heavy Traffic"

    else:
        return "Severe Congestion"

import joblib
import pandas as pd


model = joblib.load("traffic_model (2).pkl")

new_data = pd.DataFrame({
    'Junction':[1],
    'Year':[2017],
    'Month':[6],
    'Day':[15],
    'Hour':[8],
    'DayOfWeek':[3],
    'IsWeekend':[0],
    'PeakHour':[1],
    'Session':[0],
    'Prev_Hour_Vehicles':[120],
    'Prev_2Hour_Vehicles':[110],
    'Rolling_Mean_3':[115]
})

prediction = model.predict(new_data)[0]

congestion = classify_congestion(prediction)

print("Predicted Vehicles:", round(prediction))
print("Congestion Level:", congestion)

