def classify_congestion(vehicle_count):

    if vehicle_count < 50:
        return "Low Traffic"

    elif vehicle_count < 100:
        return "Moderate Traffic"

    elif vehicle_count < 200:
        return "Heavy Traffic"

    else:
        return "Severe Congestion"


routes = {
    "Route A": 180,
    "Route B": 95,
    "Route C": 60,
    "Route D": 220
}

print("Route Status")

for route, vehicles in routes.items():
    print(
        route,
        "-",
        vehicles,
        "vehicles -",
        classify_congestion(vehicles)
    )

best_route = min(routes, key=routes.get)